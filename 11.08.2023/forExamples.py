#0-20 arası çift sayıları for döngüsü ile ekrana yazdır.
print("0-20 arası çift sayılar:" )
for i in range(0,21,2):
  print(i)

#1-30 arası tek sayıları for döngüsü ile yazdır.
print("1-30 arası tek sayılar: ")
for t in range(1,30,2):
  print(t)

#3-41 arası sayıları 5'er 5'er yazdır
print("3-41 arası 5'er 5'er artan sayılar")
for b in range(3,41,5):
  print(b)

#50'den 20'ye kadar sayıalrı 3'er azaltarak yazdır
#20 dahilse range(50,19,-3)
for s in range(50,20,-3):
  print(s)